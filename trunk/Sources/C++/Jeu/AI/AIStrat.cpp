bool AIStrat::TesterPushPuckOnTheOtherSide(Partie* pPartie, NoeudMaillet* pMaillet )
{
    NoeudRondelle* wPuck = pPartie->getField()->getPuck();
    return pMaillet->isLeftSide() ? (wPuck->getPosition()[VX] > 0) : (wPuck->getPosition()[VX] < 0);
}

