#pragma once
#include "UsinePaquet.h"
#include "../Paquets/PaquetGameEvent.h"


///////////////////////////////////////////////////////////////////////////
/// @class UsinePaquetGameEvent
/// @brief Classe Factory (Usine) pour les paquets de type GameEvent
///
/// @author Mathieu Parent
/// @date 2013-03-17
///////////////////////////////////////////////////////////////////////////
class UsinePaquetGameEvent : public UsinePaquet
{
public:
    /// Constructeur par paramètres.
    inline UsinePaquetGameEvent();

	/// Fonction à surcharger pour la création d'un noeud.
	inline virtual Paquet* creerPaquet() const;


};


////////////////////////////////////////////////////////////////////////
///
/// @fn UsinePaquetGameEvent::UsinePaquetGameEvent()
///
/// Ce constructeur ne fait qu'appeler la version de la classe et base
/// et donner des valeurs par défaut aux variables membres..
///
///
/// @return Aucune (constructeur).
///
////////////////////////////////////////////////////////////////////////
inline UsinePaquetGameEvent::UsinePaquetGameEvent()
	: UsinePaquet()
{
}


////////////////////////////////////////////////////////////////////////
///
/// @fn Paquet* UsinePaquetGameEvent::creerNoeud() const
///
/// Cette fonction retourne un noeud nouvellement créé du type produit
/// par cette usine.
///
/// @return Le noeud nouvellement créé.
///
////////////////////////////////////////////////////////////////////////
Paquet* UsinePaquetGameEvent::creerPaquet() const
{
	return new PaquetGameEvent();
}


